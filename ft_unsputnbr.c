#include "ft_printf.h"

int ft_unsputnbr(unsigned int n)
{
	int		i;

	i = 1;
	if (n > 9)
	{
		i += ft_putnbr(n / 10);
		n %= 10;
	}
	if (n <= 9)
	{
		ft_putchar(n + '0');
	}
	return (i);
}